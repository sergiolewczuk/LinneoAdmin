    public Recibo(java.awt.Frame parent, boolean modal, String[] datos_alumno, String[][] datos_inscripcion) {
        super(parent, modal);
        initComponents();
        
        
        jLabel2.setText("Cod: "+ datos_alumno[0] + " - DNI: " + datos_alumno[1] + " - Apellido y Nombre: "+ datos_alumno[2]);
                
        String[][] datos_inscripcion_nuevo = new String[datos_inscripcion.length][14];
        
        for(int a = 0; a < datos_inscripcion.length; a++){
            
            // TOTAL
            
            //Double total_pagar_temp = Double.valueOf(datos_inscripcion[a][3]);
            
            total_pagar = total_pagar.add(new BigDecimal (datos_inscripcion[a][3]));
            total_debe = total_debe.add(new BigDecimal (datos_inscripcion[a][4]));
            
            datos_inscripcion_nuevo[a][0] = "Inscripcion";
            
            datos_inscripcion_nuevo[a][1] = datos_inscripcion[a][13];
            datos_inscripcion_nuevo[a][2] = datos_inscripcion[a][1];
            
            datos_inscripcion_nuevo[a][3] = datos_inscripcion[a][5] + "-" + datos_inscripcion[a][6];
            datos_inscripcion_nuevo[a][4] = datos_inscripcion[a][7] + "-" + datos_inscripcion[a][8];
            datos_inscripcion_nuevo[a][5] = df.format(Double.valueOf(datos_inscripcion[a][3]).doubleValue());
            datos_inscripcion_nuevo[a][6] = df.format(Double.valueOf(datos_inscripcion[a][4]).doubleValue());
            
            
        }
        
        datos_del_alumno = datos_alumno;
        datos_de_inscripcion = datos_inscripcion;
        
        jTextField1.setText(df.format(total_pagar));
        jTextField2.setText(df.format(total_debe));
        
        Clases.Class_editar_tabla.main(jTable1, datos_inscripcion_nuevo);

        //Hijack the keyboard manager
        manager.addKeyEventDispatcher(key);
        
        getRootPane().setBorder( BorderFactory.createLineBorder(Color.RED) );
        
        Toolkit tk = Toolkit.getDefaultToolkit();
        Dimension tamanio = tk.getScreenSize();
        this.setSize((int)tamanio.getWidth() - (int)(tamanio.getWidth()/4), (int)tamanio.getHeight() - (int)(tamanio.getHeight()/4));
        this.setLocationRelativeTo(null);
        
    }

