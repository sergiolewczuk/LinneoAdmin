    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() != -1) {
            if (jDesktopPane1.getAllFrames().length != 0) {
                JInternalFrame frame = jDesktopPane1.getAllFrames()[0];
                frame.dispose();
            }
            
            String id_grado = null; String carrera = null;
            String nombre = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
            
            if(!"".equals(nombre)){
                id_grado = jTable1.getValueAt(jTable1.getSelectedRow(), 6).toString();
                carrera = jTable1.getValueAt(jTable1.getSelectedRow(), 5).toString();
            }else{
                id_grado = null;
                carrera = jTable1.getValueAt(jTable1.getSelectedRow(), 5).toString();
            }          

            ModCursos frame = new ModCursos(id_grado, carrera);
            frame.setVisible(true);
            jDesktopPane1.add(frame);
            try {
                frame.setMaximum(true);
            } catch (PropertyVetoException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

