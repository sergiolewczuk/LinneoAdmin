    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Integer Seleccion = JOptionPane.showOptionDialog(Principal.jDesktopPane1.getSelectedFrame(), "Seleccione una Opción", "LinneoAdmin", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Guardar", "Cancelar", "Volver al Listado de Cursos"}, "Si");

        // SELECCIONAR SI ELIGIO BIEN EL CURSO O PREGUNTAR SI ESTA SEGURO
        if (Seleccion == 0) {
            if (jTextField2.getText().isEmpty() || jComboBox1.getSelectedItem() == "") {
                JOptionPane.showMessageDialog(Principal.jDesktopPane1.getSelectedFrame(), "Complete los datos correctamente.", "LinneoAdmin", JOptionPane.WARNING_MESSAGE);
            } else {
                cursos.InsertarCuentasInscripcion(niveles, jTextField2.getText(), jComboBox1.getSelectedItem().toString());
                JOptionPane.showMessageDialog(Principal.jDesktopPane1.getSelectedFrame(), "Guardado Correctamente", "LinneoAdmin", JOptionPane.INFORMATION_MESSAGE);
            }

            //  VOLVER A CARGAR LA TABLA
            cargar_tabla(niveles);
            comprobar_partida();                
        } else if (Seleccion == 1) {

            //  VOLVER A CARGAR LA TABLA
            cargar_tabla(niveles);
        } else if (Seleccion == 2) {
            if (jDesktopPane1.getAllFrames().length != 0) {
                JInternalFrame frame = jDesktopPane1.getAllFrames()[0];
                frame.dispose();
            }

            ABMCursos frame = new ABMCursos();
            frame.setVisible(true);
            jDesktopPane1.add(frame);
            try {
                frame.setMaximum(true);
            } catch (PropertyVetoException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

