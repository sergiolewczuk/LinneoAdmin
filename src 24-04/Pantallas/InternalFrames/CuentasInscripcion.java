    Boolean comprobar_partida() {
        Boolean bool = null;
        Integer debe = 0;
        Integer haber = 0;
        for (int a = 0; a < jTable1.getRowCount(); a++) {
            String valor = jTable1.getValueAt(a, 3).toString();
            if ("Debe".equals(valor)) {
                debe++;
            } else {
                haber++;
            }
        }
        
        if((debe==1 && haber==2) || (debe==0 && haber==0)){
            bool = true;
        }else{
            bool = false;
        }
        
        return bool;
    }
    
