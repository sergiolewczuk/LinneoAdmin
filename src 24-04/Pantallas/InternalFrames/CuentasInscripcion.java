    //TECLADO ACCESOS DIRECTOS
    class KeyDispatcher implements KeyEventDispatcher {

        @Override
        public boolean dispatchKeyEvent(KeyEvent e) {
            if (jframe.isActive()) {
                if (e.getID() == KeyEvent.KEY_PRESSED) {
                    if (e.getKeyCode() == 112) {
                        jButton1.doClick();
                    }
                    if (e.getKeyCode() == 113) {
                        jButton3.doClick();
                    }
                    if (e.getKeyCode() == 127){
                        Integer Seleccion = JOptionPane.showOptionDialog(Principal.jDesktopPane1.getSelectedFrame(), "Seleccione una Opción", "LinneoAdmin", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Eliminar", "Cancelar"}, "Si");
                        if(Seleccion == 0){ // ELIMINAR
                            String num_cuenta = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
                            String debe = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();

                            if ("Debe".equals(debe)) {
                                debe = "0";
                            } else {
                                debe = "1";
                            }

                            cursos.EliminarCuentasInscripcion(niveles, num_cuenta, debe);
                            JOptionPane.showMessageDialog(Principal.jDesktopPane1.getSelectedFrame(), "Se eliminaron los datos correctamente.", "LinneoAdmin", JOptionPane.INFORMATION_MESSAGE);

                            cargar_tabla(niveles);
                        }else{              // NO HACER NADA
                            
                        }
                    }
                }
            }
            return false;
        }
    }
    
    
