    class KeyDispatcher implements KeyEventDispatcher {

        @Override
        public boolean dispatchKeyEvent(KeyEvent e) {
            if (jframe.isActive()) {
                if (e.getID() == KeyEvent.KEY_PRESSED) {
                    if (e.getKeyCode() == 112) {
                        jButton1.doClick();
                    }
                    if (e.getKeyCode() == 113) {
                        jButton2.doClick();
                    }
                    if (e.getKeyCode() == 114) {
                        jButton3.doClick();
                    }
                    if (e.getKeyCode() == 115) {
                        jButton4.doClick();
                    }
                    if (e.getKeyCode() == 116) {
                        jButton5.doClick();
                    }
                }
            }
            return false;
        }
    }
    
    void cargar_curso(String curso, String carrera){
        
        if(curso != null){ // ES GRADO
            
            Object[] info = cursos.MostrarDatosGrados(curso);

            jTextField1.setText(info[2].toString() + "  -  " + info[3].toString() + "  -  " + info[4].toString());

            monto_cuota_viejo.setText(info[10].toString());    monto_anticipado_viejo.setText(info[11].toString());     // CUOTAS VIEJAS
            monto_inscripcion_viejo.setText(info[9].toString());                                                      // INSCRIPCION VIEJAS
            cant_alumnos_viejo.setText(info[12].toString());                                                           // CANT. ALUMNOS

            cod_grado = info[1].toString();
            cod_carrera = info[6].toString();
            nombre_carrera = info[7].toString();
            
        }else{                      // ES CARRERA
            Object[] info = cursos.MostrarDatosCarreras(carrera);

            jTextField1.setText(info[2].toString() + "  -  " + info[3].toString());

            monto_cuota_viejo.setText(info[5].toString());    monto_anticipado_viejo.setText(info[6].toString());     // CUOTAS VIEJAS
            monto_inscripcion_viejo.setText(info[4].toString());                                                      // INSCRIPCION VIEJAS
            cant_alumnos_viejo.setText(info[7].toString());                                                           // CANT. ALUMNOS

            cod_carrera = info[1].toString();
            nombre_carrera = info[2].toString();
        }
        
    }
    
