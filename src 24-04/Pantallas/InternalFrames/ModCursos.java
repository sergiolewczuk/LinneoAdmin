    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Integer Seleccion = JOptionPane.showOptionDialog(Principal.jDesktopPane1.getSelectedFrame(), "Confirmar modificaciones?", "LinneoAdmin", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Confirmar", "Cancelar"}, "Si");
        // SELECCIONAR SI ELIGIO BIEN EL CURSO O PREGUNTAR SI ESTA SEGURO
        if (Seleccion == 0) {
            
            if (monto_cuota_nuevo.getText().isEmpty()) {
                monto_cuota_nuevo.setText(monto_cuota_viejo.getText());
            }
            if (monto_anticipado_nuevo.getText().isEmpty()) {
                monto_anticipado_nuevo.setText(monto_anticipado_viejo.getText());
            }
            if (monto_inscripcion_nuevo.getText().isEmpty()) {
                monto_inscripcion_nuevo.setText(monto_inscripcion_viejo.getText());
            }
            if (cant_alumnos_nuevo.getText().isEmpty()) {
                cant_alumnos_nuevo.setText(cant_alumnos_viejo.getText());
            }
            
            cursos.ModificarImporteCursoIndividual(new BigDecimal(monto_cuota_nuevo.getText()), new BigDecimal (monto_anticipado_nuevo.getText()), new BigDecimal (monto_inscripcion_nuevo.getText()), new BigDecimal (cant_alumnos_nuevo.getText()), cod_grado, cod_carrera);
            JOptionPane.showMessageDialog(Principal.jDesktopPane1.getSelectedFrame(), "Modificaciones Guardadas", "LinneoAdmin", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

